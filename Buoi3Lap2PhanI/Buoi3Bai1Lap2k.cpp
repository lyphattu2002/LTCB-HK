#include <iostream>
using namespace std;
int main() {
    cout << "Cac so nguyen tu 11 den 99 thoa man dieu kien: \n";
    for (int i = 11; i <= 99; ++i) {
        int tens_digit = i / 10;
        int ones_digit = i % 10;
        int product = tens_digit * ones_digit;
        int sum = tens_digit + ones_digit;
        if (product == 2 * sum) {
            cout << i <<endl;
        }
    }

    return 0;
}

